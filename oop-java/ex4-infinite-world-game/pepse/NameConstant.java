package pepse;

/**
 * Contains string constants used for tagging different game objects.
 * These tags are used for object identification, collision handling,
 * and layer management throughout the game.
 */
public class NameConstant {
    /** Tag for the player character */
    public static final String AVATAR = "avatar";

    /** Tag for the basic ground blocks */
    public static final String GROUND = "ground";

    /** Tag for collectible fruit objects on trees */
    public static final String FRUIT = "fruit";

    /** Tag for tree trunk blocks */
    public static final String TREE = "tree";

    /** Tag for the sky background */
    public static final String SKY = "sky";

    /** Tag for the energy level display */
    public static final String ENERGY_INDICATOR = "energyIndicator";

    /** Tag for terrain blocks below the surface */
    public static final String TERRAIN = "terrain";

    /** Tag for generic block objects */
    public static final String BLOCK = "block";

    /** Tag for tree leaf objects */
    public static final String LEAF = "leaf";

    /** Tag for the top layer of terrain blocks */
    public static final String TERRAIN_TOP = "groundTop";

    /** Tag for rain drop objects */
    public static final String DROP = "drop";
}
